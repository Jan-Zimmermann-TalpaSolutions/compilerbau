/**
 * The type Input character.
 *
 * @author jan on 21.03.2020
 * @project compilerbau
 */
class InputCharacter {

    /**
     * The Character.
     */
    char character;
    /**
     * The Line.
     */
    int line;

    /**
     * Instantiates a new Input character.
     *
     * @param c the c
     * @param l the l
     */
    InputCharacter(char c, int l) {
        this.character = c;
        this.line = l;
    }
}
